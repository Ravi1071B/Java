package array;

public class SumofArray {

	public static void main(String[] args) {

		 int sum=0;
         int[]a= {10,20,30};
         
         for(int i=0;i<=a.length;i++)
         {
        	  sum=sum+a[i];
        	  
        	  System.out.println("Sum of Array is:"+ sum);
         
         }
              
	}

}
